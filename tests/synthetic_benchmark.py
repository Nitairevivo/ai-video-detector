"""
VerifAI Synthetic Benchmark — creates test videos with FFmpeg
that mimic real AI-generated and real-camera video properties.
No external downloads needed.
"""
import os, sys, json, subprocess, tempfile, time
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))
from analyzer import extract_features

FFMPEG = "ffmpeg"
RESULTS = []

def run(cmd):
    return subprocess.run(cmd, capture_output=True, timeout=30).returncode == 0

def make_video(path, **meta):
    """Create a short test video with FFmpeg and inject metadata."""
    tags = " ".join(f'-metadata {k}="{v}"' for k, v in meta.items())
    cmd = (
        f'{FFMPEG} -y -f lavfi -i testsrc2=size=480x854:rate=24:duration=3 '
        f'-f lavfi -i sine=frequency=440:duration=3 '
        f'-c:v libx264 -c:a aac -shortest {tags} "{path}" -loglevel error'
    )
    return os.system(cmd) == 0

def make_ai_video(path, tool_name, encoder=None):
    """Video that mimics AI-generated output — software tag with AI tool name."""
    meta = {"software": tool_name, "comment": f"Generated by {tool_name}"}
    if encoder:
        meta["encoder"] = encoder
    return make_video(path, **meta)

def make_real_video(path, device="iPhone 15 Pro"):
    """Video that mimics camera-captured footage."""
    return make_video(path,
        software=device,
        encoder="Apple VideoToolbox",
        creation_tool="com.apple.quicktime",
        comment="Shot on iPhone",
    )

def make_edited_video(path, edit_tool="CapCut"):
    """Real video edited with AI tool."""
    return make_video(path,
        software=f"{edit_tool} 10.2.0",
        encoder="MediaCodec",
        comment=f"Edited with {edit_tool}",
    )

def make_clean_video(path):
    """Video with no identifying metadata — unknown origin."""
    return make_video(path)


def test_video(path, expected_verdict, label):
    t0 = time.time()
    r = extract_features(path)
    elapsed = round(time.time() - t0, 2)

    predicted = r.verdict
    correct = predicted == expected_verdict

    icon = "✅" if correct else "❌"
    print(f"  {icon} [{label:<30}] expected={expected_verdict:<12} got={predicted:<12} "
          f"conf={r.confidence:.0%}  ({elapsed}s)")
    if not correct:
        print(f"       method: {r.method}")

    return {
        "label": label,
        "expected": expected_verdict,
        "predicted": predicted,
        "confidence": round(r.confidence, 3),
        "ai_tool": r.ai_tool,
        "edit_tool": r.edit_tool,
        "method": r.method,
        "correct": correct,
    }


