FFMPEG = shutil.which("ffmpeg") or "ffmpeg"


def download(url: str, out: str) -> bool:
    try:
        req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0"})
        with urllib.request.urlopen(req, timeout=30) as r:
            data = r.read(20 * 1024 * 1024)
        if len(data) < 5000:
            return False
        with open(out, "wb") as f:
            f.write(data)
        return True
    except Exception:
        return False


def make_synthetic(metadata: dict, duration: float = 4.0, ai_looking: bool = True) -> str:
    """Create a synthetic video with specific metadata injected."""
    tmp = tempfile.mktemp(suffix=".mp4")
    vf = "unsharp=5:5:0.8" if ai_looking else "noise=alls=12:allf=t"
    meta_args = []
    for k, v in metadata.items():
        meta_args += ["-metadata", f"{k}={v}"]
    cmd = [
        FFMPEG, "-y",
        "-f", "lavfi", "-i", f"testsrc2=size=480x270:rate=24",
        "-f", "lavfi", "-i", "sine=f=440",
        "-t", str(duration),
        "-vf", vf,
        "-c:v", "libx264", "-crf", "20",
        "-c:a", "aac",
        *meta_args,
        tmp, "-loglevel", "error"
    ]
    subprocess.run(cmd, capture_output=True, timeout=30)
    return tmp if os.path.exists(tmp) else None


def extract(path: str, is_ai: bool, label: str, url: str = "") -> dict:
    result = extract_features(path)
    return {
        "is_ai": is_ai,
        "label": label,
        "url": url,
        "confidence": result.confidence,
        "verdict": result.verdict,
        "ai_tool": result.ai_tool,
        "feature_vector": result.feature_vector,
        "signals": {k: float(v) if isinstance(v, (int, float)) else 0.0
                    for k, v in result.signals.items()},
    }


def main():
    out_path = Path(__file__).parent.parent / "data" / "ml_training.json"
    existing = []
    if out_path.exists():
        with open(out_path) as f:
            existing = json.load(f)
    existing_labels = {(e["label"], e["url"]) for e in existing}

    samples = list(existing)

    print("=" * 60)
    print("  VerifAI Training Dataset Builder")
    print("=" * 60)

    # ── Real footage from Wikimedia ────────────────────────────────
    print(f"\n[REAL] Downloading {len(REAL_VIDEOS)} real videos...")
    for url, label in REAL_VIDEOS:
        if (label, url) in existing_labels:
            print(f"  SKIP {label}")
            continue
        tmp = tempfile.mktemp(suffix=".webm")
        print(f"  {label:<25} ", end="", flush=True)
        if download(url, tmp):
            try:
                s = extract(tmp, False, label, url)
                samples.append(s)
                print(f"✅ real ({s['confidence']:.0%})")
            except Exception as e:
                print(f"❌ extract error: {e}")
            finally:
                try: os.unlink(tmp)
                except: pass
        else:
            print("⚠️  download failed")

    # ── Synthetic AI videos (metadata injected) ────────────────────
    print(f"\n[AI] Creating {len(AI_METADATA_SETS)} synthetic AI-signature videos...")
    for meta in AI_METADATA_SETS:
        label = f"synthetic_ai_{meta['software'].split()[0].lower()}"
        if (label, "") in existing_labels:
            print(f"  SKIP {label}")
            continue
        print(f"  {label:<35} ", end="", flush=True)
        tmp = make_synthetic(meta, ai_looking=True)
        if tmp:
            try:
                s = extract(tmp, True, label)
                samples.append(s)
                icon = "✅" if s["verdict"] == "ai_generated" else "⚠️"
                print(f"{icon} {s['verdict']} ({s['confidence']:.0%}) tool={s['ai_tool']}")
            except Exception as e:
                print(f"❌ {e}")
            finally:
                try: os.unlink(tmp)
                except: pass
        else:
            print("❌ ffmpeg failed")

    # ── Synthetic camera videos (false-positive prevention) ────────
    print(f"\n[REAL+CAM] Creating {len(CAMERA_METADATA_SETS)} camera-metadata videos...")
    for meta in CAMERA_METADATA_SETS:
        label = f"synthetic_cam_{meta['software'].split()[0].lower()}"
        if (label, "") in existing_labels:
            print(f"  SKIP {label}")
            continue
        print(f"  {label:<35} ", end="", flush=True)
        tmp = make_synthetic(meta, ai_looking=False)
        if tmp:
            try:
                s = extract(tmp, False, label)
                samples.append(s)
                icon = "✅" if s["verdict"] == "real" else "⚠️ FP!"
                print(f"{icon} {s['verdict']} ({s['confidence']:.0%})")
            except Exception as e:
                print(f"❌ {e}")
            finally:
                try: os.unlink(tmp)
                except: pass

    # ── Save and train ─────────────────────────────────────────────
    with open(out_path, "w") as f:
        json.dump(samples, f, indent=2)

    ai_n = sum(1 for s in samples if s["is_ai"])
    real_n = len(samples) - ai_n
    print(f"\n{'='*60}")
    print(f"  Dataset: {len(samples)} total — {ai_n} AI, {real_n} Real")

    # Submit to API for training
    if ai_n >= 5 and real_n >= 5:
        print("\n  Training ML classifier via API...")
        try:
            req = urllib.request.Request(
                "https://ai-video-detector-production-a305.up.railway.app/train",
                data=b"", method="POST",
                headers={"Content-Type": "application/json"}
            )
            with urllib.request.urlopen(req, timeout=30) as r:
                result = json.loads(r.read())
            print(f"  ✅ Model trained: {result}")
        except Exception as e:
            print(f"  ❌ Train API failed: {e}")
    else:
        print(f"  ⚠️  Need more data (AI={ai_n}, Real={real_n})")

    print(f"\n  Saved: {out_path}")


if __name__ == "__main__":
    main()
